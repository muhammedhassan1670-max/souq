export const getProductById = (id: string | number): Product | undefined => {
  return products.find(p => p.id === id);
};

export const getProductsByCategory = (category: string): Product[] => {
  return products.filter(p => p.category === category);
};

export const getOfferProducts = (): Product[] => {
  return products.filter(p => p.isOffer);
};

export const getLocalProducts = (): Product[] => {
  return products.filter(p => p.isLocalProduct);
};

export const getAvailableProducts = (): Product[] => {
  return products.filter(p => p.available);
};

export const searchProducts = (query: string): Product[] => {
  const normalizedQuery = query.toLowerCase().trim();
  if (!normalizedQuery) return products;
  
  return products.filter(p => {
    const nameMatch = p.name.toLowerCase().includes(normalizedQuery);
    const categoryMatch = p.category.toLowerCase().includes(normalizedQuery);
    const keywordMatch = p.keywords.some(k => k.toLowerCase().includes(normalizedQuery));
    const tagMatch = p.tags.some(t => t.toLowerCase().includes(normalizedQuery));
    return nameMatch || categoryMatch || keywordMatch || tagMatch;
  });
};
